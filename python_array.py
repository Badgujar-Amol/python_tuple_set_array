# define array
lang = ['html', 'css', 'boostrap', 'python']
print(lang)

# access element
lang = ['html', 'css', 'boostrap', 'python', 'javascript', 'java']
print(lang[1])
print(lang[5])
print(lang[1:3])
print(lang[-1])

# modify value
lang = ['html', 'css', 'boostrap', 'python']
lang[2] = 'java'
print(lang)

# len() method
lang = ['html', 'css', 'boostrap', 'python', 'javascript', 'java']
print(len(lang))

# append method
List1 = ['html', 'css', 'boostrap', 'python', 'javascript', 'java']
List1.append('c++')
print(List1)

# remove method
sample = [10, 20, 30, 40, 50]
sample.remove(40)
print(sample)